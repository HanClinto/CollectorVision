#!/usr/bin/env python3
"""
run_experiment.py
-----------------
Automates the WebGPU vs WASM correctness experiment end-to-end.

Connects to an Android device via ADB (phone plugged in, USB debugging on,
Chrome open) or falls back to a local Chrome instance, serves browser_test.html,
waits for it to finish, reads the results, and prints a structured report showing
exactly which ONNX operators produce wrong output under the WebGPU EP.

Requirements
------------
    pip install -r requirements.txt
    playwright install chromium

For Android:
    - USB debugging enabled on device (Settings → Developer Options)
    - Chrome open on device with at least one tab
    - Device connected via USB

Usage
-----
    # Try Android first, fall back to local Chrome:
    python run_experiment.py

    # Local Chrome only (desktop GPU — useful sanity check, wrong platform):
    python run_experiment.py --local

    # Force rebuild of ONNX models even if they already exist:
    python run_experiment.py --rebuild

    # Also capture WGSL shaders generated by ORT (for upstream bug report):
    python run_experiment.py --capture-shaders

    # Adjust timeout if models load slowly:
    python run_experiment.py --timeout 180
"""

import argparse
import importlib.util
import json
import os
import subprocess
import sys
import threading
import time
import urllib.request
from datetime import datetime
from http.server import HTTPServer, SimpleHTTPRequestHandler
from pathlib import Path

EXPERIMENT_DIR = Path(__file__).parent
CDP_PORT = 9222

# Chrome's DevTools socket name.  Tried in order; first success wins.
CHROME_DEVTOOLS_SOCKETS = [
    "localabstract:chrome_devtools_remote",
    "localabstract:com.android.chrome_devtools_remote",
    "localabstract:com.chrome.beta_devtools_remote",
]


# ─────────────────────────────────────────────────────────────────────────────
# Prerequisites
# ─────────────────────────────────────────────────────────────────────────────

def check_prerequisites() -> None:
    missing = []
    try:
        import playwright  # noqa: F401
    except ImportError:
        missing.append(("playwright", "pip install playwright && playwright install chromium"))
    for pkg in ("onnx", "numpy", "onnxruntime"):
        try:
            __import__(pkg)
        except ImportError:
            missing.append((pkg, f"pip install {pkg}"))
    if missing:
        print("Missing prerequisites — install and retry:")
        for pkg, cmd in missing:
            print(f"  [{pkg}]  {cmd}")
        sys.exit(1)


# ─────────────────────────────────────────────────────────────────────────────
# Model building
# ─────────────────────────────────────────────────────────────────────────────

def build_models(force: bool = False) -> None:
    expected_json = EXPERIMENT_DIR / "expected_outputs.json"
    onnx_files    = list(EXPERIMENT_DIR.glob("test_*.onnx"))

    if not force and expected_json.exists() and len(onnx_files) >= 4:
        print("Models present (use --rebuild to regenerate).")
        return

    print("Building minimal ONNX test models…")
    old_cwd = os.getcwd()
    os.chdir(EXPERIMENT_DIR)
    try:
        spec = importlib.util.spec_from_file_location(
            "create_minimal_model", EXPERIMENT_DIR / "create_minimal_model.py"
        )
        mod = importlib.util.module_from_spec(spec)
        spec.loader.exec_module(mod)
        mod.build_all(EXPERIMENT_DIR)
    finally:
        os.chdir(old_cwd)
    print(f"  expected_outputs.json + 4 .onnx files written to {EXPERIMENT_DIR}")


# ─────────────────────────────────────────────────────────────────────────────
# HTTP server
# ─────────────────────────────────────────────────────────────────────────────

class _QuietHandler(SimpleHTTPRequestHandler):
    def log_message(self, *_):
        pass  # silence per-request noise


def start_server() -> int:
    """Start a local file server in a daemon thread; return the bound port."""
    handler = lambda *args: _QuietHandler(*args, directory=str(EXPERIMENT_DIR))
    server  = HTTPServer(("127.0.0.1", 0), handler)
    port    = server.server_address[1]
    threading.Thread(target=server.serve_forever, daemon=True).start()
    return port


# ─────────────────────────────────────────────────────────────────────────────
# ADB / Android
# ─────────────────────────────────────────────────────────────────────────────

def _adb(serial: str, *args, timeout: int = 10) -> subprocess.CompletedProcess:
    return subprocess.run(
        ["adb", "-s", serial, *args],
        capture_output=True, text=True, timeout=timeout,
    )


def find_android_device() -> "tuple[str | None, str]":
    """Return (serial, model_name) for the first connected device, or (None, '')."""
    try:
        r = subprocess.run(
            ["adb", "devices", "-l"],
            capture_output=True, text=True, timeout=5,
        )
        for line in r.stdout.splitlines()[1:]:
            if "\tdevice" in line:
                parts     = line.split()
                serial    = parts[0]
                model     = next((p.split(":")[1] for p in parts if p.startswith("model:")), "Android")
                return serial, model
    except (FileNotFoundError, subprocess.TimeoutExpired):
        pass
    return None, ""


def setup_android(serial: str, server_port: int) -> bool:
    """
    1. Launch Chrome if not already open.
    2. Forward ADB port → Chrome DevTools socket.
    3. Reverse the HTTP server port so it looks like localhost on the device.
    4. Return True if the DevTools endpoint is responding.
    """
    # Ensure Chrome is running on the device.
    _adb(serial, "shell", "am", "start", "-a", "android.intent.action.VIEW",
         "-d", "about:blank", "com.android.chrome")
    time.sleep(2)

    # Try each known socket name until one forwards successfully.
    forwarded = False
    for socket in CHROME_DEVTOOLS_SOCKETS:
        r = _adb(serial, "forward", f"tcp:{CDP_PORT}", socket)
        if r.returncode == 0:
            forwarded = True
            break
    if not forwarded:
        return False

    # Make our HTTP server accessible as localhost on the device.
    _adb(serial, "reverse", f"tcp:{server_port}", f"tcp:{server_port}")

    # Poll until Chrome's DevTools responds.
    for _ in range(15):
        try:
            with urllib.request.urlopen(
                f"http://localhost:{CDP_PORT}/json/version", timeout=2
            ):
                return True
        except Exception:
            time.sleep(0.5)
    return False


def teardown_adb(serial: str, server_port: int) -> None:
    subprocess.run(["adb", "-s", serial, "forward", "--remove", f"tcp:{CDP_PORT}"],
                   capture_output=True)
    subprocess.run(["adb", "-s", serial, "reverse", "--remove", f"tcp:{server_port}"],
                   capture_output=True)


# ─────────────────────────────────────────────────────────────────────────────
# Browser / Playwright
# ─────────────────────────────────────────────────────────────────────────────

def run_tests(test_url: str, android: bool, timeout_s: int) -> dict:
    from playwright.sync_api import sync_playwright

    with sync_playwright() as p:
        if android:
            print(f"  Connecting via CDP to http://localhost:{CDP_PORT}…")
            browser = p.chromium.connect_over_cdp(f"http://localhost:{CDP_PORT}")
            # Use the existing context so we don't need camera/notification
            # permissions to be granted fresh.
            context = browser.contexts[0] if browser.contexts else browser.new_context()
        else:
            print("  Launching local Chrome (headless)…")
            browser = p.chromium.launch(headless=True)
            context = browser.new_context()

        page = context.new_page()
        print(f"  → {test_url}")
        page.goto(test_url, timeout=30_000)

        print(f"  Waiting for tests to complete (timeout {timeout_s}s)…")
        page.wait_for_function("() => window.__testDone === true",
                               timeout=timeout_s * 1_000)

        results = page.evaluate("""() => {
            const r = window.__testResults ?? {};
            const ai = r.adapterInfo ?? {};
            return {
                adapterInfo: {
                    vendor:       ai.vendor        ?? 'unknown',
                    architecture: ai.architecture  ?? 'unknown',
                    device:       ai.device        ?? 'unknown',
                    description:  ai.description   ?? '',
                },
                userAgent:       navigator.userAgent,
                tests:           r.tests   ?? [],
                summary:         r.summary ?? {},
                capturedShaders: window.__capturedShaders ?? null,
            };
        }""")

        page.close()
        # close() disconnects without killing Chrome when connected via CDP.
        browser.close()

    return results


# ─────────────────────────────────────────────────────────────────────────────
# Report
# ─────────────────────────────────────────────────────────────────────────────

# Smallest model that exercises each operator set (in ascending complexity).
MODEL_OPS = {
    "test_single_conv.onnx":         "Conv",
    "test_conv_bn_relu.onnx":        "Conv › BatchNorm › ReLU",
    "test_two_conv.onnx":            "Conv › ReLU › Conv",
    "test_global_avgpool_gemm.onnx": "Conv › ReLU › GlobalAveragePool › Flatten › Gemm",
}

GREEN  = "\033[92m"
YELLOW = "\033[93m"
RED    = "\033[91m"
BOLD   = "\033[1m"
DIM    = "\033[2m"
RESET  = "\033[0m"


def print_report(results: dict, android: bool, device_model: str) -> None:
    ai = results.get("adapterInfo", {})
    ua = results.get("userAgent", "")
    chrome_ver = next(
        (seg.split("/")[1] for seg in ua.split() if seg.startswith("Chrome/")), "?"
    )

    sep = "─" * 62
    print(f"\n{BOLD}{sep}{RESET}")
    print(f"{BOLD}  ORT WebGPU vs WASM — results{RESET}")
    print(sep)
    print(f"  Platform : {'Android (' + device_model + ')' if android else 'local Chrome (desktop)'}")
    print(f"  GPU      : {ai.get('vendor','?')} / {ai.get('architecture','?')} / {ai.get('device','?')}")
    print(f"  Chrome   : {chrome_ver}")
    print(sep)

    tests = results.get("tests", [])
    for t in tests:
        name   = t.get("name", "?")
        status = t.get("status", "skip")
        wdiff  = t.get("wasmMaxDiff")
        gdiff  = t.get("webgpuMaxDiff")
        ops    = MODEL_OPS.get(name, "?")

        if status == "pass":
            icon, label = f"{GREEN}✓{RESET}", f"{GREEN}PASS{RESET}"
        elif status == "warn":
            icon, label = f"{YELLOW}△{RESET}", f"{YELLOW}WARN — WebGPU wrong{RESET}"
        elif status == "fail":
            icon, label = f"{RED}✗{RESET}",   f"{RED}FAIL{RESET}"
        else:
            icon, label = f"{DIM}–{RESET}",   f"{DIM}SKIP{RESET}"

        print(f"  {icon} {name}  [{ops}]")
        print(f"      {label}")
        if wdiff is not None:
            ok = f"{GREEN}✓{RESET}" if wdiff <= 1e-3 else f"{RED}✗{RESET}"
            print(f"      WASM:   max diff {wdiff:.2e} {ok}")
        if gdiff is not None:
            ok = f"{GREEN}✓{RESET}" if gdiff <= 5e-3 else f"{RED}✗ (broken){RESET}"
            print(f"      WebGPU: max diff {gdiff:.2e} {ok}")

    s = results.get("summary", {})
    print(sep)
    print(
        f"  {GREEN}{s.get('pass',0)} pass{RESET}  "
        f"{YELLOW}{s.get('warn',0)} warn (WebGPU wrong){RESET}  "
        f"{RED}{s.get('fail',0)} fail{RESET}  "
        f"{DIM}{s.get('skip',0)} skip{RESET}"
    )
    print(sep)

    warns = [t for t in tests if t.get("status") == "warn"]
    if warns:
        first = warns[0]
        ops   = MODEL_OPS.get(first["name"], "?")
        print(f"\n{BOLD}  ► Broken operator(s) narrowed to: {ops}{RESET}")
        print(f"    First model where WebGPU diverges: {first['name']}")
        print()
        for t in tests:
            mark = "✓" if t.get("status") == "pass" else ("✗ BROKEN" if t.get("status") == "warn" else "–")
            arrow = "→ " if t.get("status") not in ("pass", "skip") else "  "
            print(f"    {arrow}{mark}  {t['name']}  [{MODEL_OPS.get(t['name'],'?')}]")
        print()
        print("  Next steps to file an upstream report:")
        print("  1. Attach results-<timestamp>.json and the .onnx file to the issue")
        print("  2. github.com/microsoft/onnxruntime/issues — labels: ep:webgpu, platform:android")
        print("  3. See README.md § Upstream Contribution Strategy for a full issue template")
        if results.get("capturedShaders"):
            print("  4. WGSL shaders are in results.json → capturedShaders — include them too")
    elif s.get("pass", 0) == len(tests) and tests:
        print(f"\n  {GREEN}All tests pass — WebGPU EP is correct on this platform.{RESET}")
        if not android:
            print(f"  {DIM}(This is expected on desktop; test on Android to reproduce the bug.){RESET}")

    print()


def write_results(results: dict) -> Path:
    ts   = datetime.now().strftime("%Y%m%d-%H%M%S")
    path = EXPERIMENT_DIR / f"results-{ts}.json"
    with open(path, "w") as f:
        json.dump(results, f, indent=2)
    try:
        rel = path.relative_to(Path.cwd())
    except ValueError:
        rel = path
    print(f"  Results: {rel}")
    return path


# ─────────────────────────────────────────────────────────────────────────────
# CLI
# ─────────────────────────────────────────────────────────────────────────────

def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(
        description=__doc__,
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    p.add_argument("--local",           action="store_true",
                   help="Use local Chrome only (skip ADB detection)")
    p.add_argument("--rebuild",         action="store_true",
                   help="Rebuild ONNX models even if they already exist")
    p.add_argument("--capture-shaders", action="store_true",
                   help="Capture WGSL shaders generated by ORT (for upstream report)")
    p.add_argument("--timeout",         type=int, default=120, metavar="S",
                   help="Test timeout in seconds (default: 120)")
    return p.parse_args()


def main() -> int:
    args = parse_args()

    print("WebGPU Android ARM experiment runner")
    print("─" * 40)

    check_prerequisites()
    build_models(force=args.rebuild)

    server_port = start_server()
    test_url    = f"http://localhost:{server_port}/browser_test.html"
    if args.capture_shaders:
        test_url += "?capture_shaders=1"
    print(f"Serving at {test_url}")

    android      = False
    device_model = "local"
    serial       = None

    if not args.local:
        serial, device_model = find_android_device()
        if serial:
            print(f"Found Android device: {device_model} ({serial})")
            ok = setup_android(serial, server_port)
            if ok:
                android = True
                print("  ADB forwarding active.")
            else:
                print(
                    "  ⚠ ADB forward failed.\n"
                    "    Make sure Chrome is open on the device and USB debugging is enabled.\n"
                    "    Falling back to local Chrome."
                )
                serial = None
        else:
            print("No Android device found via ADB — using local Chrome.")

    try:
        results = run_tests(test_url, android, args.timeout)
    finally:
        if serial:
            teardown_adb(serial, server_port)

    print_report(results, android, device_model)
    write_results(results)

    s = results.get("summary", {})
    return 1 if (s.get("warn", 0) + s.get("fail", 0)) > 0 else 0


if __name__ == "__main__":
    sys.exit(main())
